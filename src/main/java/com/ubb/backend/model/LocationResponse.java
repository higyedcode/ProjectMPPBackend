package com.ubb.backend.model;

public class LocationResponse {
    private String lat;
    private String lon;

    // getters and setters
    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLon() {
        return lon;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }
}